#!/usr/bin/env bash
# Builds VoiceMode Monitor as a .app bundle, installs to ~/Applications,
# launches it. Mirrors the spacegrids build script pattern.
#
# Modes:
#   release (default) — Developer ID Application cert + hardened runtime
#                       (notarizable). After build, run notarize_app.sh
#                       to submit to Apple notarization.
#   dev (VOICEMODE_DEV_BUILD=1) — first available cert, no hardened runtime.
#
# Optional env:
#   VOICEMODE_NO_SPARKLE=1   — build without the Sparkle update framework
#                              (faster local rebuilds; SparkleBridge stubs out).
#   VOICEMODE_SKIP_LAUNCH=1  — assemble the bundle but don't `open` it
#                              (CI / automation friendly).

set -euo pipefail

ROOT="$(cd "$(dirname "$0")" && pwd)"
cd "$ROOT"

APP_NAME="VoiceMode Monitor"
EXEC_NAME="VoiceModeMenuBar"
BUNDLE_ID="com.williamruiz.voicemode-monitor"
INSTALL_DIR="/Applications"   # System Applications — visible in Finder's default Applications view.
APP_BUNDLE="${INSTALL_DIR}/${APP_NAME}.app"

DEV_BUILD="${VOICEMODE_DEV_BUILD:-0}"
SKIP_LAUNCH="${VOICEMODE_SKIP_LAUNCH:-0}"

echo "==> swift build (release)"
swift build -c release

EXEC_PATH=".build/release/${EXEC_NAME}"
if [ ! -f "$EXEC_PATH" ]; then
    echo "ERROR: build output not found at $EXEC_PATH" >&2
    exit 1
fi

echo "==> assembling .app bundle"
rm -rf "$APP_BUNDLE"
mkdir -p "${APP_BUNDLE}/Contents/MacOS"
mkdir -p "${APP_BUNDLE}/Contents/Resources"
mkdir -p "${APP_BUNDLE}/Contents/Frameworks"

cp "$EXEC_PATH" "${APP_BUNDLE}/Contents/MacOS/${EXEC_NAME}"
cp "Resources/Info.plist" "${APP_BUNDLE}/Contents/Info.plist"

# SPM doesn't add the standard macOS app-bundle rpath that lets the binary
# find frameworks at Contents/Frameworks/. Add it explicitly so dyld resolves
# Sparkle.framework when the app launches. install_name_tool is idempotent —
# silently no-ops if the rpath already exists from a prior run, which is what
# we want when re-running the script over an existing dev build.
install_name_tool -add_rpath "@executable_path/../Frameworks" \
    "${APP_BUNDLE}/Contents/MacOS/${EXEC_NAME}" 2>/dev/null || true

# App icon: drop the generated .icns into Resources/. Info.plist references it
# via CFBundleIconFile=AppIcon. Generated by `swift scripts/generate_icon.swift`.
if [ -f "Resources/AppIcon.icns" ]; then
    cp "Resources/AppIcon.icns" "${APP_BUNDLE}/Contents/Resources/AppIcon.icns"
else
    echo "WARN: Resources/AppIcon.icns missing — run 'swift scripts/generate_icon.swift'."
fi

# Sparkle framework: SPM places it at .build/release/Sparkle.framework. We
# copy it into Contents/Frameworks/ and re-sign on the way out (Sparkle's
# own helper executables — Autoupdate.app, Updater.app — must each be signed
# with the same identity as the host app for the runtime to trust them).
SPARKLE_FRAMEWORK=".build/release/Sparkle.framework"
if [ -d "$SPARKLE_FRAMEWORK" ]; then
    echo "==> bundling Sparkle.framework"
    cp -R "$SPARKLE_FRAMEWORK" "${APP_BUNDLE}/Contents/Frameworks/"
else
    echo "WARN: Sparkle.framework not found at $SPARKLE_FRAMEWORK (built without Sparkle?)"
fi

echo "==> codesigning (mode: $([ "$DEV_BUILD" = "1" ] && echo dev || echo release))"
if [ "$DEV_BUILD" = "1" ]; then
    # Dev build: first available signing identity, no hardening. Faster
    # iteration; not suitable for distribution.
    IDENTITY=$(security find-identity -v -p codesigning | awk '/[0-9]+\)/ {print $2; exit}')
    if [ -z "$IDENTITY" ]; then
        echo "WARN: no codesigning identity — skipping signature"
    else
        # Sign nested helpers FIRST (deepest first), then the framework, then the app.
        if [ -d "${APP_BUNDLE}/Contents/Frameworks/Sparkle.framework" ]; then
            find "${APP_BUNDLE}/Contents/Frameworks/Sparkle.framework" \
                -type d \( -name "*.app" -o -name "*.xpc" \) \
                -print0 | while IFS= read -r -d '' nested; do
                codesign --force --sign "$IDENTITY" "$nested"
            done
            codesign --force --sign "$IDENTITY" "${APP_BUNDLE}/Contents/Frameworks/Sparkle.framework"
        fi
        codesign --force --sign "$IDENTITY" "$APP_BUNDLE"
    fi
else
    # Release build: Developer ID Application cert + hardened runtime + secure timestamp.
    # The hardened runtime is required by Apple notarization.
    #
    # We also pin the cert by SHA-1 hash via the env override
    # `VOICEMODE_RELEASE_IDENTITY` if you want to disambiguate when multiple
    # Developer ID certs exist on the keychain. Otherwise we pick the first one.
    if [ -n "${VOICEMODE_RELEASE_IDENTITY:-}" ]; then
        IDENTITY="$VOICEMODE_RELEASE_IDENTITY"
    else
        IDENTITY=$(security find-identity -v -p codesigning \
            | grep "Developer ID Application" \
            | head -1 \
            | awk -F\" '{print $2}')
    fi
    if [ -z "$IDENTITY" ]; then
        echo "ERROR: no Developer ID Application cert. Re-run with VOICEMODE_DEV_BUILD=1 for self-signed." >&2
        exit 1
    fi
    echo "    using identity: $IDENTITY"

    # Optional entitlements file. Hardened runtime adds default restrictions;
    # we use an entitlements file when present so future capabilities (e.g.
    # JIT for an embedded interpreter) are explicit, not silent.
    ENTITLEMENTS_ARG=()
    if [ -f "Resources/VoiceModeMonitor.entitlements" ]; then
        ENTITLEMENTS_ARG=(--entitlements "Resources/VoiceModeMonitor.entitlements")
    fi

    # Sign nested helpers + framework FIRST (deepest first) under hardened runtime.
    if [ -d "${APP_BUNDLE}/Contents/Frameworks/Sparkle.framework" ]; then
        echo "==> signing Sparkle helpers"
        find "${APP_BUNDLE}/Contents/Frameworks/Sparkle.framework" \
            -type d \( -name "*.app" -o -name "*.xpc" \) \
            -print0 | while IFS= read -r -d '' nested; do
            codesign --force --sign "$IDENTITY" \
                --options runtime --timestamp \
                "$nested"
        done
        codesign --force --sign "$IDENTITY" \
            --options runtime --timestamp \
            "${APP_BUNDLE}/Contents/Frameworks/Sparkle.framework"
    fi

    # Then sign the host app (covers the executable).
    codesign --force --sign "$IDENTITY" \
        --options runtime --timestamp \
        "${ENTITLEMENTS_ARG[@]}" \
        "$APP_BUNDLE"

    # Verify — fails the build if anything is wrong (better here than at notarytool time).
    echo "==> verifying signature"
    codesign --verify --deep --strict --verbose=2 "$APP_BUNDLE"
fi

echo "==> installing to $INSTALL_DIR"
mkdir -p "$INSTALL_DIR"

if [ "$SKIP_LAUNCH" = "1" ]; then
    echo "==> done (skipping launch)."
else
    echo "==> done. launching..."
    open "$APP_BUNDLE"
fi

echo ""
echo "App: $APP_BUNDLE"
echo "Look for the waveform icon in your menu bar (top-right)."
echo "  • outlined waveform = idle"
echo "  • red waveform = some app is using your microphone"
echo ""
if [ "$DEV_BUILD" = "1" ]; then
    echo "Built in DEV mode (no hardened runtime, no notarization)."
    echo "For a distributable build, re-run without VOICEMODE_DEV_BUILD=1."
else
    echo "Next step (for distribution): ./notarize_app.sh \"$APP_BUNDLE\""
fi
